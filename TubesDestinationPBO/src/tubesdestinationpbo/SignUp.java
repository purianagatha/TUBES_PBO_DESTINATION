    private void signUpBottonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpBottonActionPerformed
        // TODO add your handling code here:
        try{
            String username = UserName.getText();
            String email = Email.getText();
            String password = Password.getText();
            JOptionPane.showMessageDialog(null, "Akun Berhasil Dibuat");
            Pengguna user = new Pengguna(username, email, password);
            user.signUp();
            UserName.setText("");
            Email.setText("");
            Password.setText("");
        }catch (SQLException E){
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, E);
        }
    }//GEN-LAST:event_signUpBottonActionPerformed

